#include<stdio.h>
//program execution starts from main function
int main(){//main function starts
	int a, b;//declaration of variable
	a = 3, b = 5;//initialize vaule to variables
	int sum;//declaration of variable
	sum = a + b;//formula or equation of sum
	printf("Sum is = %d \n", sum);//printing sum value only
	printf("Sum of a & b is = %d \n", sum);//printing sum value only	
	printf("Sum of %d + %d is = %d \n", a, b, sum);//printing a, b & sum value
	return 0;
}//main function ends

