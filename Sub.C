#include<stdio.h>
//program execution starts from main function
int main(){//main function starts
	int a, b;//declaration of variable
	a = 3, b = 5;//initialize vaule to variables
	int sub;//declaration of variable
	sub = a - b;//formula or equation of subtraction
	printf("Subtraction is = %d \n", sub);//printing sub value only
	printf("Subtraction of a & b is = %d \n", sub);//printing sub value only	
	printf("Subtraction of %d - %d is = %d \n", a, b, sub);//printing a, b & sub value
	return 0;
}//main function ends

